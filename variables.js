// There are 4 types of decleared the variables
/* 1.Using var
2.Using let
3.Using const
4.without using keyword: Automatically 
*/
// -------------------------------------------------------------------

// Using var -older version 
var num = 10; //using var, value can be change
console.log(num);

// Using let - latest version of var 
let num2 = 20;  
num2 +=5 // using let, value can be change
console.log(num2);

// Using const - that means value can't be change 
const num3 = 30; // using const value can't be change
console.log(num3);



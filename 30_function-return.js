function checkEven(num){
    if(num % 2 ===0){
        return true;
        
    }
    else{
        return false
        
    }
}
console.log(checkEven(43));

// odd
function checkOdd(num2){
    if(num2 % 2 ===1){
        return true;
        
    }
    else{
        return false
        
    }
}
console.log(checkOdd(43));





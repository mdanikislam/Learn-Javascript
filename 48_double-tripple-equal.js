const a = 2;
const b = "2";
// double equal  - check only the values
if (a == b) {
  console.log("Values and type is equal");
} else {
  console.log("values is equal but type is not equal");
}

// tripple equal - check both the type and values
if (a === b) {
  console.log("Values and type is equal");
} else {
  console.log("values is equal but type is not equal");
}
